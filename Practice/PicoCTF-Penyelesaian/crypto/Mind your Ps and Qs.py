# faktorisasi prima n pertama menggunakan https://www.alpertron.com.ar/ECM.HTM
p, q = 1899107986527483535344517113948531328331, 674357869540600933870145899564746495319033

# mencari phi(n)
phi = (p - 1) * (q - 1)

